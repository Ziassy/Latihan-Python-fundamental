# ARGUMENT SPECIFIERS
# %s - String
# %d - Integers
# %f - Bilangan Desimal
# %.<digit>f - Bilangan desimal dengan sejumlah digit angka dibelakang koma.
# %x/%X - Bilangan bulat dalam representasi Hexa (huruf kecil/huruf besar)
nama = 'Pauziah'
umur = 19
print('Hallo %s!' % nama)
print('Perkenalkan nama saya %s!, umur saya %d tahun' % (nama, umur))